class Track {
  String id;
  String title;
  String artist;
  String album;
  String duration;
  String coverUrl;
  String audioUrl;

  Track({
    required this.id,
    required this.title,
    required this.artist,
    required this.album,
    required this.duration,
    required this.coverUrl,
    required this.audioUrl,
  });
}
